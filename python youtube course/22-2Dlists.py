# 1:28:43. A 2D list is a list of lists

drinks = ["coffee", "soda", "tea"]
dinner = ["pizza", "hamburger", "hotdog"]
dessert = ["cake", "ice cream"]

food = [drinks, dinner, dessert]

print(food)
print(food[0]) # displays the first list
print(food[0][0]) # displays the first element within the first list.